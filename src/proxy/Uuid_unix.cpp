#ifdef __FreeBSD__
#   include <uuid.h>
#else
#   include <uuid/uuid.h>
#endif

#include "proxy/Uuid.h"


void Uuid::create(char *out, size_t size)
{
    uuid_t id;

#   ifdef __FreeBSD__
    uuid_create(&id, nullptr);
    uuid_to_string(&id, &out, nullptr);
#   else
    uuid_generate(id);
    uuid_unparse_lower(id, out);
#   endif
}
